using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace SudokuSolver
{
    class Program
    {
        static void Main(string[] args)
        {
            int[,] example = new int[,]
            {
                {5,3,0,0,7,0,0,0,0},
                {6,0,0,1,9,5,0,0,0},
                {0,9,8,0,0,0,0,6,0},
                {8,0,0,0,6,0,0,0,3},
                {4,0,0,8,0,3,0,0,1},
                {7,0,0,0,2,0,0,0,6},
                {0,6,0,0,0,0,2,8,0},
                {0,0,0,4,1,9,0,0,5},
                {0,0,0,0,8,0,0,7,9},
            };

            WriteTable(example);

            Sudoku s = new Sudoku(example);
            if (s.Solution())
                WriteTable(example);
            else
                Console.WriteLine("There is no solution!");

            Console.ReadLine();
        }

        static void WriteTable(int[,] a)
        {
            for (int i = 0; i < a.GetLength(0); i++)
            {
                for (int j = 0; j < a.GetLength(1); j++)
                    Console.Write(((a[i, j] == 0) ? "_" : a[i, j].ToString()) + " ");
                Console.WriteLine();
            }
            Console.WriteLine();
        }
    }
}
